# Lendo dois números inteiros
x = int(input("Digite o primeiro número (x): "))
y = int(input("Digite o segundo número (y): "))

# Realizando operações e imprimindo resultados
print("Soma:", x + y)
print("Subtração:", x - y)
print("Multiplicação:", x * y)
print("Divisão:", x / y)
print("Divisão inteira:", x // y)
print("Exponenciação:", x ** y)
